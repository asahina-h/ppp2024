	@GetMapping("/")
	public String showForm(@RequestParam(value = "month", required = false) String month, Model model)
	{
		List<String> years = getYearsRange();
		model.addAttribute("years", years);

		List<String> months = getMonths();
		model.addAttribute("months", months);

		if (month == null || month.isEmpty())
		{
			month = "1";
		}

		List<Integer> daysInMonth = getDaysInMonth(month);
		model.addAttribute("daysInMonth", daysInMonth);
		model.addAttribute("selectedMonth", month);

		return "index";
	}

	private List<String> getYearsRange()
	{
		List<String> years = new ArrayList<>();
		int currentYear = LocalDate.now().getYear();
		for (int i = currentYear; i >= 1900; i--)
		{
			years.add(String.valueOf(i));
		}
		return years;
	}

	private List<String> getMonths()
	{
		List<String> months = new ArrayList<>();
		for (int i = 1; i <= 12; i++)
		{
			months.add(String.valueOf(i));
		}
		return months;
	}

	private List<Integer> getDaysInMonth(String month)
	{
		List<Integer> daysInMonth = new ArrayList<>();
		int maxDays = 31;
		switch (month)
		{
		case "2":
			int year = LocalDate.now().getYear();
			if ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0))
			{
				maxDays = 29; // leap year
			} else
			{
				maxDays = 28;
			}
			break;
		case "4":
		case "6":
		case "9":
		case "11":
			maxDays = 30;
			break;
		default:
			maxDays = 31;
		}
		for (int i = 1; i <= maxDays; i++)
		{
			daysInMonth.add(i);
		}
		return daysInMonth;
	}

	@GetMapping("/fortune")
	public String getFortune(@RequestParam(value = "year", required = false) String year,
			@RequestParam(value = "month", required = false) String month,
			@RequestParam(value = "day", required = false) String day,
			Model model)
	{

		if (year == null || year.isEmpty())
		{
			year = String.valueOf(LocalDate.now().getYear());
		}

		if (month == null || month.isEmpty())
		{
			month = "1";
		}

		if (day == null || day.isEmpty())
		{
			day = "1";
		}

		int yearValue = Integer.parseInt(year);
		List<AnimalFortune> fortunes = getAnimalFortunes();
		int fortuneIndex = (yearValue % 12);
		fortuneIndex = fortuneIndex % 12;

		if (fortuneIndex < 0)
		{
			fortuneIndex += fortunes.size();
		}

		AnimalFortune animalFortune = fortunes.get(fortuneIndex); // 修正箇所: "fortune" -> "animalFortune"
		model.addAttribute("animalFortune", animalFortune); // 修正: モデルにanimalFortuneを設定
		model.addAttribute("year", year);
		model.addAttribute("month", month);
		model.addAttribute("day", day);

		return "fortune";
	}

